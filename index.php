		    	<div class="container">
	            <p>
			Virginie Duhamel a étudié à Paris 8 où elle a obtenu sa maitrise d’Histoire de l’Art.<br>
			Elle a également suivi les ateliers des Beaux Arts et participé aux cours d’arts modernes de l’école et galerie Edouard Manet à Gennevilliers, suite à quoi elle a obtenu un atelier dans le vieux Gennevilliers.
		      </p>
		      <hr>
		      <p>
			Elle a participé à des concours nationaux comme celui des Arts et Métiers catégorie « Peinture » et obtenu en 2012 le premier prix, ainsi qu’en 2005, le second prix du Salon des jeunes artistes contemporains du 92.
		      </p>
		      <hr>
		      <p>
			Son travail consiste à créer des contrastes visuels, entre portraits dessinés à la mine de plomb et motifs colorés sur papier. Afin de faire ressortir son travail sur le visage, elle procède « a contrario », c’est-à-dire en attirant d’abord notre œil sur des motifs en couleurs, évoquant parfois le dessin d’enfant, pour finalement l’attirer vers des visages à priori plus en retraits, mais tout aussi présents que ses motifs, et souvent emprunts d’une forme de mélancolie, ou de réflexion tournée vers l’intériorité.
		      </p>
		      <hr>
		      <p> 
			On peut également y voir un travail sur le masque façon « commedia dell’arte », comme plusieurs personnes l’ont souligné.
		      </p>
		      <hr>
		      <p>
			Toutes ses dernières compositions sont donc des collages sur carton ou toile, l’idée étant de proposer un travail avant tout sur le motif, la couleur et le portrait, en utilisant des procédés visuels contradictoires.
		      </p>
		      <hr>
		      <p>
			Auparavant, elle peignait à l’huile sur bois, mais finalement cette technique a cédé sa place à des moyens créatifs plus rapides et plus légers, comme les crayons et les feutres, type marqueurs, sur feuilles A4 ou A3.
		      </p>	
